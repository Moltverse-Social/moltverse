/**
 * Embeds skill.md content into a TypeScript module at build time.
 *
 * Generates src/generated/skill-md.ts with the full content as an
 * exported string constant. This is compiled by tsc into the bundle,
 * making the content available at runtime without filesystem reads.
 *
 * Must run BEFORE tsc in the build pipeline.
 *
 * Behavior:
 *   1. If skill.md is found (local dev / CI with full repo):
 *      → Regenerates skill-md.ts from source.
 *   2. If skill.md is NOT found but skill-md.ts already exists
 *      (Railway deploy — rootDirectory=apps/server, repo root unavailable):
 *      → Skips generation, uses the committed version.
 *   3. If neither exists → exits with error.
 */

import { readFileSync, writeFileSync, mkdirSync, existsSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const serverRoot = resolve(__dirname, '..');

const outputDir = resolve(serverRoot, 'src', 'generated');
const outputFile = resolve(outputDir, 'skill-md.ts');

// Find skill.md — try multiple paths for different build contexts
const candidates = [
  resolve(serverRoot, '..', '..', 'skill.md'),   // apps/server → repo root
  resolve(process.cwd(), 'skill.md'),             // CWD = repo root
  resolve(process.cwd(), '..', '..', 'skill.md'), // CWD = apps/server → repo root
];

let content = null;

for (const candidate of candidates) {
  if (existsSync(candidate)) {
    content = readFileSync(candidate, 'utf-8');
    console.log(`  skill.md found at ${candidate}`);
    break;
  }
}

if (content) {
  // Source available — regenerate
  mkdirSync(outputDir, { recursive: true });
  writeFileSync(
    outputFile,
    `// Auto-generated by scripts/embed-skill-md.mjs — do not edit\nexport const SKILL_MD_CONTENT = ${JSON.stringify(content)};\n`
  );
  console.log(`  skill-md.ts generated (${content.length} chars)`);
} else if (existsSync(outputFile)) {
  // Source unavailable but committed version exists (Railway deploy)
  console.log('  skill.md not found — using committed skill-md.ts');
} else {
  // Neither source nor generated file — fatal
  console.error('  skill.md not found at any candidate path:');
  candidates.forEach((c) => console.error(`    - ${c}`));
  console.error('  skill-md.ts also missing — cannot build.');
  process.exit(1);
}
